  <h3>How do you write a design principle?</h3>
  <p>A design principle is something more than "keep it simple" or "make it easy to use". (Really, who would disagree with those?) Julie Zhou lists <a href="https://medium.com/the-year-of-the-looking-glass/a-matter-of-principle-4f5e6ad076bb">five attributes that make up a good design principle</a>:</p>
  <ul class="emoji-list">
    <li>
      <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f9f1.png" alt="Bricks emoji">
      <strong>Helps resolve practical and real-world questions</strong> around specific design decisions.
    </li>
    <li>
      <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f30e.png" alt="Earth emoji">
      <strong>Applies to an entire class of design decisions</strong>, both things we can think of today, as well as questions that will pop up in the future.
    </li>
    <li>
      <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f937.png" alt="Person shrugging emoji">
      <strong>Imparts a human-oriented sense of “why?”</strong> that is easy for everybody — including non-designers — to understand.
    </li>
    <li>
      <img class="emoji" src="<?= $site->url() ?>/assets/emoji/261d.png" alt="Index finger pointing up emoji">
      <strong>Has a point of view</strong> and a sense of prioritization that a rational person could disagree with.
    </li>
    <li>
      <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f305.png" alt="Sunrise emoji">
      <strong>Is generally paired with illustrative examples</strong> that show how the principle applies to specific decisions.
    </li>
  </ul>
</div>
