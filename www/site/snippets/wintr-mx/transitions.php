  <ul>
    <li><strong>Go to the <span class="color-subdominant">subdominant (up a fourth)</span>.</strong> As key changes go, this may be the most common of all time. Bridges and middle sections of songs are often in the subdominant.</li>
    <li><strong>Go to the <span class="color-dominant">dominant (up a fifth)</span>.</strong> The dominant tends to feel unresolved, as it’s often the chord that comes right before a resolution. Composers use this fact to build tension and suspense before resolving back to the home key.</li>
    <li><strong>Go <span class="color-halfstepup">up a half-step</span>.</strong> This happens a lot toward the end of big musicals. It ratchets up the intensity, but execise restraint: <a href="https://www.youtube.com/watch?v=Js-6Lx-SZso">overusing it</a> dilutes its effect and can get eye-rolly pretty quickly.</li>
    <li><strong>Go <span class="color-halfstepdown">down a half-step</span>.</strong> This kinda feels like sinking into a big, plush armchair. It’s less common than the upward half-step transition, but more interesting, in my opinion.</li>
    <li><strong>Go <span class="color-wholestep">up or down a whole-step</span>.</strong> Moving stepwise achieves a similar effect to modulating by half-step, but feels a bit more stable and triumphant.</li>
    <li><strong>Go to the relative or parallel minor (not pictured).</strong> When moving between major and minor keys, it's common to move in parallel (from C major to C minor—root stays the same but key signature changes) or relative (from C major to A minor—root changes but key signature stays the same).</li>
  </ul>
