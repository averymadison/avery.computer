<div class="post-callout">
  <h3>Before we begin, some helpful terms:</h3>
  <ul class="emoji-list">
    <li data-emoji="🖥">
      <dl>
        <dt>dashboard</dt>
        <dd>the interface which allows Swiftype users to customize their search engines; users of our user’s site will never see this</dd>
      </dl>
    </li>
    <li data-emoji="⚙️">
      <dl>
        <dt>search engine</dt>
        <dd>the code that powers search for a particular app or website; Swiftype users can have one or many  engines</dd>
      </dl>
    </li>
    <li data-emoji="🗂">
      <dl>
        <dt>indexing</dt>
        <dd>the process of collecting, parsing, and storing data so that it can be quickly retrieved</dd>
      </dl>
    </li>
    <li data-emoji="🎚">
      <dl>
        <dt>weights</dt>
        <dd>a feature allowing users to assign levels of importance to titles, descriptions, and other metadata in order to control their relevance in search results</dd>
      </dl>
    </li>
    <li data-emoji="🔝">
      <dl>
        <dt>result ranking</dt>
        <dd>a feature allowing users to manually reposition search results for a query</dd>
      </dl>
    </li>
  </ul>
</div>
