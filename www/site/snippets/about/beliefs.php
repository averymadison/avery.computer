<h3>Good design stems from:</h3>
<ul class="emoji-list">
  <li>
    <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f4ac.png">
    <strong>Understanding.</strong> Before prescribing solutions, research why things are the way they are. There’s a reason. (But that doesn’t mean the reason is right.) Listen to experts in whatever form they take.
  </li>
  <li>
    <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f645.png">
    <strong>Principle.</strong> Value well-being over profitability and engagement. A quick win isn't worth sacrificing your convictions.
  </li>
  <li>
    <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f64f.png">
    <strong>Order.</strong> Problems feel less daunting when you’re not surrounded by chaos. Clean up messes, even ones you didn’t make. Your teammates will thank you.
  </li>
  <li>
    <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f984.png">
    <strong>Inclusivity.</strong> Design with, and for, others who are not like you. You’ll create better solutions, and seeing life from a new perspective is a wonderful gift.
  </li>
  <li>
    <img class="emoji" src="<?= $site->url() ?>/assets/emoji/1f60c.png">
    <strong>Humility.</strong> Sometimes you’ll be wrong. Accept it early and move on. Sometimes the real solution lies beyond your power. That’s ok, too. Suggest a path forward.
  </li>
</ul>
</div>
